`划分目录结构
 主要在src里面
    assets(资源) 里面分为:
        css 和 image 

    components 里面再分为:
        common   放可以在项目之外使用的组件
        content  放和业务有关的组件

    views(视图) 里面放主要视图的组件，如主页等
        home      主页
        category  分类
    
    router(路由) 放路由相关的东西

    store(vuex)  放vuex(公共状态管理)的文件夹
    
    network      放网络相关的东西
 
    commom       放公共相关的 JS 文件
        const.js   公共的常量
        utils.js   公共的方法
 
 
 
 
 `